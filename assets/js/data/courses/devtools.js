コードは「書く時間」より「**読まれる時間**」のほうがずっと長いものです。半年後の自分や、チームの誰かが必ず読みます。読みやすいコードは**保守性(変更のしやすさ)**を高め、バグを減らします。動けばよいのではなく、**読めてこそ良いコード**です。

